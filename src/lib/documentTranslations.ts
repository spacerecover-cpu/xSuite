export type TranslationKey = keyof typeof DOCUMENT_TRANSLATIONS['ar'];

export function getTranslation(
  key: TranslationKey,
  languageCode: LanguageCode | null
): string | null {
  if (!languageCode) return null;
  const translations = DOCUMENT_TRANSLATIONS[languageCode] as Record<string, string> | undefined;
  return translations?.[key] || null;
}

export function isRTLLanguage(languageCode: LanguageCode | null): boolean {
  if (!languageCode) return false;
  return isRTLLanguageShared(languageCode);
}

export function formatBilingualText(
  englishText: string,
  translatedText: string | null,
  _isRTL: boolean = false
): string {
  if (!translatedText) return englishText;
  return `${englishText} | ${translatedText}`;
}
